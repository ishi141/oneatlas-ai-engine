"use client";

import {
  Copy,
  Download,
  Check,
} from "lucide-react";

interface Props {
  copied: boolean;
  active: string;
  currentJson: unknown;
  setCopied: (value: boolean) => void;
}

export default function ResultHeader({
  copied,
  active,
  currentJson,
  setCopied,
}: Props) {

  async function copyJson() {

    await navigator.clipboard.writeText(
      JSON.stringify(currentJson, null, 2)
    );

    setCopied(true);

    setTimeout(() => {

      setCopied(false);

    }, 2000);

  }

  function downloadJson() {

    const blob = new Blob(
      [
        JSON.stringify(
          currentJson,
          null,
          2
        ),
      ],
      {
        type: "application/json",
      }
    );

    const url = URL.createObjectURL(blob);

    const a = document.createElement("a");

    a.href = url;
    a.download = `${active}.json`;
    a.click();

    URL.revokeObjectURL(url);

  }

  return (

    <div
      className="
        flex
        flex-col
        gap-8
        lg:flex-row
        lg:items-center
        lg:justify-between
      "
    >

      {/* Left */}

      <div>

        <div
          className="
            inline-flex
            items-center
            rounded-full
            border
            border-red-900/40
            bg-red-950/20
            px-3
            py-1
            text-xs
            font-semibold
            uppercase
            tracking-wider
            text-red-300
          "
        >

          Generated Output

        </div>

        <h2
          className="
            mt-5
            text-3xl
            font-bold
            tracking-tight
            text-white
          "
        >

          Architecture Result

        </h2>

        <p
          className="
            mt-3
            max-w-3xl
            text-sm
            leading-7
            text-zinc-500
          "
        >

          Browse every layer generated by the pipeline including
          intent extraction, schema generation, application
          specification, validation report and execution cost.

        </p>

      </div>

      {/* Actions */}

      <div className="flex flex-wrap gap-3">

        <button

          onClick={copyJson}

          className="
            inline-flex
            items-center
            gap-2
            rounded-xl
            border
            border-white/5
            bg-[#171717]
            px-5
            py-3
            text-sm
            font-medium
            text-zinc-300
            transition-all
            duration-300
            hover:bg-[#202020]
            hover:text-white
          "

        >

          {copied ? (

            <>

              <Check
                size={18}
                className="text-emerald-400"
              />

              Copied

            </>

          ) : (

            <>

              <Copy size={18} />

              Copy JSON

            </>

          )}

        </button>

        <button

          onClick={downloadJson}

          className="
            inline-flex
            items-center
            gap-2
            rounded-xl
            bg-gradient-to-r
            from-red-700
            to-red-600
            px-5
            py-3
            text-sm
            font-semibold
            text-white
            shadow-lg
            shadow-red-900/30
            transition-all
            duration-300
            hover:scale-[1.02]
            hover:from-red-600
            hover:to-red-500
          "

        >

          <Download size={18} />

          Download JSON

        </button>

      </div>

    </div>

  );

}